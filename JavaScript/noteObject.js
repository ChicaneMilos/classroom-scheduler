export class noteObject {
    constructor(id, name, study, start_hour, start_minute, end_hour, end_minute)
    {
        this.id = id;
        this.name = name;
        this.study = study;
        this.start_hour = start_hour;
        this.start_minute = start_minute;
        this.end_hour = end_hour;
        this.end_minute = end_minute;
    }
}